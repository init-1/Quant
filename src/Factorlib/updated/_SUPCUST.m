        function factorTS = build(o, secIds, startDate, endDate)
            
            %% Generate dates as backtest dates and data in principle should be aligned against dates
            dates = o.genDates(startDate, endDate, o.targetFreq);    % 'Busday', 0);
            factorTS = myfints(dates,nan(length(dates),numel(secIds)),QuantId2FieldId(secIds));
            
            %% load static supplier customer mapping
            mapdata = o.LoadSupCustMap();
            if sum(ismember(mapdata.sids,secIds)) > 0
                idx = ~ismember(mapdata.sids,secIds);
                mapdata.sids(idx) = [];
                mapdata.cids(idx) = [];
                mapdata.cgics(idx) = [];
                
                sDate = datestr(addtodate(datenum(startDate),-1,'M'),'yyyy-mm-dd');
                
                sids = mapdata.sids;
                cids = mapdata.cids;
                usids = unique(sids);
                [ucids, ucidx] = unique(mapdata.cids);
                
                gics = mapdata.cgics(ucidx);
                gics = floor(str2num(cell2mat(gics))/1000000);
                
                % Generate signal - Relative sector returns
                secPrice = o.loadItem(ucids,'D001410446',sDate,endDate);
                crtnfts = Price2Return(secPrice,1);
                crtnftsTS_rel = o.calculation(crtnfts, gics, dates);
                
                [crtnftsTS_rel,factorTS] = aligndates(crtnftsTS_rel,factorTS,dates);
                
                for n=1:numel(usids)
                    cids_n = cids(strcmpi(sids,usids(n)));
                    [~, cust_idx_tobe_avg] = ismember(ucids,cids_n);
                    cust_idx_tobe_avg(cust_idx_tobe_avg > 0) = 1;
                    s_idx = strcmpi(secIds,usids(n));
                    factorTS(:,s_idx) = nanmean(crtnftsTS_rel(:,logical(cust_idx_tobe_avg)),2);
                end
            end
        end
        
        
        function [factorTS, priceDateStruct] = buildLive(o, secIds, endDate)
            
            startDate = datestr(addtodate(datenum(endDate),-4,'M'),'yyyy-mm-dd');
            factorTS = o.build(secIds, startDate, endDate, 'M');
            factorTS = factorTS(end,:);
            
            % Set priceDateStruct appropriately
            priceDateStruct = [];
            
        end
        function crtnfts_rel = calculation(crtnfts, gics, dates)
            
            num2strcell = @(x,fmt) strrep(mat2cell(num2str(x', fmt),ones(size(x',1),1)),' ','');
            ucids = unique(fieldnames(crtnfts,1));
            ugics = unique(gics);
            ugics(ugics == 99) = [];
            ugics = num2strcell(ugics', 'S_%2.2d');
            
            % Prepare the GICS series
            gics = repmat(gics',length(dates),1);
            gicsfts = myfints(dates, gics, ucids);
            gicsfts(gicsfts == 99) = nan;
            
            sectorrtnfts = myfints(dates,nan(length(dates),length(ugics)),ugics');
            
            [gicsfts, crtnfts, sectorrtnfts] = aligndates(gicsfts, crtnfts, sectorrtnfts, dates);
            
            gicsfts(isnan(fts2mat(crtnfts))) = nan;
            crtnfts_rel = crtnfts;
            crtnfts_rel(:,:) = nan;
            
            for i=1:length(ugics)
                sectorid = str2num(ugics{i}(3:end));
                crtnfts_t = crtnfts;
                idx = fts2mat(gicsfts == sectorid);
                crtnfts_t(idx == 0) = nan;
                sectorrtnfts(:,ugics(i)) = nanmean(crtnfts_t,2);
                for t=1:length(dates)
                    crtnfts_rel(t,idx(t,:)) = bsxfun(@minus, crtnfts(t,idx(t,:)), sectorrtnfts(t,ugics(i)));
                end
            end
        end
