classdef PDFDoc
    properties (Constant)
        LOGOFILE = 'TMP_ING_LOGO.pdf';
        LF = char(10);
    end
    
    properties (Access = private)
        filename
    end
    
    methods
        
        function o = PDFDoc(filename, title, option)
            if nargin < 3, option = '10pt,twoside,a4paper'; end
            if nargin < 2
                [~,title] = fileparts(filename);
                title = strrep(title, '_', '\_'); % special trate to filename only
            end
            
            o.filename = filename;
            fid = fopen([filename '.tex'], 'w');
            FTSASSERT(fid > 0, 'Invalid PDF object');
            fwrite(fid, ['\documentclass[' option ',pdftex]{article}' o.LF ...
                settings(title) ...
                '\begin{document}' o.LF o.LF]);
            fclose(fid);
        end
        
        function status = run(o, nRunTimes)
            if nargin < 2, nRunTimes = 1; end
            
            fid = fopen([o.filename '.tex'], 'a');
            FTSASSERT(fid > 0, 'Invalid PDF object');
            fwrite(fid, [o.LF '\end{document}' o.LF]);
            fclose(fid);
            
            TEXMF = o.getTEXMF;
            status = -1;
            if exist([TEXMF 'miktex\bin\texify.exe'], 'file')
                if exist([o.filename '.pdf'], 'file')
                    tmpfid = fopen([o.filename '.pdf'], 'w');
                    if tmpfid < 0
                        TRACE.Warn([o.filename '.pdf '...
                            'already exists and is being used by another program\n'...
                            'Can NOT generate new report file.\n']);
                        return;
                    end
                    fclose(tmpfid);
                end
                pop(o.LOGOFILE);
                status = system([TEXMF 'miktex\bin\texify.exe -b -q -I. -l latex -p ' o.filename '.tex > nul 2>nul']);
                [outpath, fname] = fileparts(o.filename);
                if exist([fname '.pdf'], 'file')
                    for i = 1:nRunTimes-1
                        system([TEXMF 'miktex\bin\texify.exe -b -q -I. -l latex -p ' o.filename '.tex > nul 2>nul']);
                    end
                    status = system([TEXMF 'miktex\bin\texify.exe -b -q -I. -l latex -p ' o.filename '.tex > nul 2>nul']);
                    system(['move ' fname '.pdf ' outpath ' > nul']);
                end
                delete(o.LOGOFILE);
                if exist([fname '.out'], 'file'), delete([fname '.out']); end
            end
        end
        
        function write(o, rawStr)
            fid = fopen([o.filename '.tex'], 'a');
            FTSASSERT(fid > 0, 'Invalid PDF object');
            fwrite(fid, rawStr);
            fclose(fid);
        end
        
        function writeln(o, rawStr)
            o.write([rawStr o.LF]);
        end
        
        function table(o, caption, columnTitles, body, columnFormat, orientation, notes, isLongTable)
            if nargin < 8, isLongTable = true; end
            if nargin < 7, notes = ''; end
            if nargin < 6, orientation = 'normal'; end
            if nargin < 5, columnFormat = ''; end
            
            fid = fopen([o.filename '.tex'], 'a');
            FTSASSERT(fid > 0, 'Invalid PDF object');
            
            [head, tail] = tabframe(caption, columnTitles, columnFormat, isLongTable, '', notes, size(body,2));
            if strcmpi(orientation, 'landscape')
                fwrite(fid, ['\begin{landscape}' o.LF]);
            end
            fwrite(fid, head);
            
            [nrow,~] = size(body);
            for i = 1:nrow
                line = body(i, cellfun(@ischar, body(i,:)));
                fprintf(fid, '%s &', line{1:end-1});
                fwrite(fid, [line{end} '\tabularnewline' o.LF]);
            end
            
            fwrite(fid, tail);
            if strcmpi(orientation, 'landscape')
                fwrite(fid, ['\end{landscape}' o.LF o.LF]);
            end
            
            fclose(fid);
        end
        
        function figure(o, figFileNames, fmtStr, orientation, numFigurePerLine, numLinePerPage)
            if nargin < 6, numLinePerPage = 1; end
            if nargin < 5, numFigurePerLine = 1; end
            if nargin < 4, orientation = 'normal'; end
            if nargin < 3, fmtStr = ''; end
            
            fid = fopen([o.filename '.tex'], 'a');
            FTSASSERT(fid > 0, 'Invalid PDF object');
            
            if strcmpi(orientation, 'landscape')
                fwrite(fid, ['\begin{landscape}' o.LF]);
            end
            
            figureCounter = 0;
            for i = 1:length(figFileNames)
                if mod(figureCounter, numFigurePerLine*numLinePerPage) == 0
                    if figureCounter > 0
                        fwrite(fid, ['\end{figure}' o.LF '\clearpage' o.LF o.LF]);
                    end
                    fwrite(fid, [o.LF '\begin{figure}' o.LF '\vspace*{-.5cm}\centering' o.LF ...
                        '\setcounter{subfigure}{' num2str(figureCounter) '}' o.LF]);
                end
                
                fname = figFileNames{i};
                if exist([fname '.eps'], 'file')
                    status = system([o.getTEXMF 'miktex\bin\epstopdf.exe ' fname '.eps']);
                    if status == 0
                        delete([fname '.eps']);
                    end
                end
                if exist([fname '.pdf'], 'file')
                    posn = mod(figureCounter, numFigurePerLine);
                    if posn == 0 && numFigurePerLine == 1  % both beginning and end of line
                        beginningThing = '\hspace*{-0.35cm}';
                        endingThing = '\\';
                    elseif posn == 0 % just beginning of line
                        beginningThing = '\hspace*{-0.35cm}';
                        endingThing = '\nolinebreak';
                    elseif posn == numFigurePerLine-1  % just end of line
                        beginningThing = '';
                        endingThing = '\\';
                    else  % middle of line
                        beginningThing = '';
                        endingThing = '\nolinebreak';
                    end
                    figureCounter = figureCounter + 1;
                    
                    fwrite(fid, [beginningThing ...
                        '\subfloat['  escTeX(fname) ...          % sub caption
                        '\label{fig:' num2str(figureCounter) '}]'... % and label
                        '{\includegraphics[' fmtStr ']' ...       % figure specific fmt
                        '{' fname '.pdf}}' ...                    % figure file name
                        endingThing o.LF]);
                else
                    warning('PDF:FIGURE', [fname '.pdf does not exist']);
                end
            end
            
            if figureCounter
                fwrite(fid, ['\end{figure}\clearpage' o.LF o.LF]);
            end
            if strcmpi(orientation, 'landscape')
                fwrite(fid, ['\end{landscape}' o.LF]);
            end
            
            fclose(fid);
        end
        
    end % of methods
    
    methods (Static)
        function body = format(body, scope, varargin)
            % Usage:
            %   body = format(body, scope, 'bkcolor', 'green', 'forecolor', 'white', 'bold'...)
            % Allowable attributes are:
            %   forecolor     followed by a color name
            %   bkcolor       followed by a color name
            %   bold
            %   italic
            %   other LaTeX command, such as '{\normal %s}', '{\footnotesize %s}', '\texttt{%s}', etc.
            % Color names:
            %   black       darkgray    lime        pink        violet
            %   blue        gray        magenta     purple      white
            %   brown       green       olive       red         yellow
            %   cyan        lightgray   orange      teal
            % Colors specific to ING
            %   ingorange   inglblue    ingdblue    inggrey
            
            fmtstr = '%s';
            i = 1;
            I = length(varargin);
            while i <= I
                f = varargin{i};
                switch f
                    case 'bkcolor'
                        i = i + 1;
                        fmt = ['\cellcolor{' varargin{i} '}%s'];
                    case 'forecolor'
                        i = i + 1;
                        fmt = ['\textcolor{' varargin{i} '}{%s}'];
                    case 'bold'
                        fmt = '\textbf{%s}';
                    case 'italic'
                        fmt = '\textit{%s}';
                    otherwise
                        fmt = f;
                end
                fmtstr = strrep(fmt, '%s', fmtstr);
                i = i + 1;
            end
            
            body(scope) = cellfun(@(x) {strrep(fmtstr,'%s',x)}, body(scope));
        end
        
        function TEXMF = getTEXMF
            TEXMF = getenv('TEXMF');
            if isempty(strtrim(TEXMF))
                TEXMF = ['Y:\' getenv('USERNAME') '\TeX\'];
            end
        end
    end  % of methods (Static)
    
end  % of classdef

function str = escTeX(str)
str = regexprep(str, '[&%]', '\\$0');
end

function [head, tail, rowcolors] = tabframe(caption, columnTitles, columnDesc, isLongTable, label, notes, nColumn)
% Check parameters
if nargin < 7
    FTSASSERT(iscell(columnTitle), 'Since columnTitle is not a cell of strings, we can detect number of columns\n'); 
    nColumn = numel(columnTitles);
end

if nargin < 6 || isempty(strtrim(notes))
    notes = '';
else
    notes = ['\multicolumn{' num2str(nColumn,'%d') '}{l}{\footnotesize ' notes '}\\' PDFDoc.LF];
end

if nargin < 5 || isempty(strtrim(label))
    label = '';
else
    label = ['\label{' label '}'];
end

if nargin < 4
    isLongTable = false;
end

if nargin < 3 || isempty(strtrim(columnDesc))  % generate a default column description
    columnDesc = repmat('c', 1, nColumn);
end

% Format columnTitles
delimiter  = {' & '};   % make it a cell to preserve trailing space when using strcat()

if ischar(columnTitles)
    title = columnTitles;
else
    columnTitles = strcat(escTeX(columnTitles), delimiter);
    title = [columnTitles{:}];
    title(end-2:end) = [];   % we don't need the last ' & '
end

if isLongTable
    head = [...
        '\begin{longtable}[c]{' columnDesc '}' PDFDoc.LF ...
        '\caption{' caption label '}\\' PDFDoc.LF ...
        '\toprule' PDFDoc.LF ...
        title '\\' PDFDoc.LF ...
        '\midrule' PDFDoc.LF...
        '\endfirsthead' PDFDoc.LF...
        '\caption[]{' caption ' ... continued} \\' PDFDoc.LF...
        '\toprule' PDFDoc.LF...
        title '\\' PDFDoc.LF...
        '\midrule' PDFDoc.LF...
        '\endhead' PDFDoc.LF...
        '\bottomrule' PDFDoc.LF...
        '\multicolumn{' num2str(nColumn,'%d') '}{r}{to be continued ...}\\' PDFDoc.LF...
        '\endfoot' PDFDoc.LF...
        '\bottomrule' PDFDoc.LF...
        notes ...
        '\endlastfoot' PDFDoc.LF];
    tail = ['\end{longtable}' PDFDoc.LF];
else
    head = [...
        '\begin{table}[htb]' PDFDoc.LF...
        '\centering' PDFDoc.LF ...
        '\caption{' caption label '}' PDFDoc.LF ...
        '\begin{tabular}{' columnDesc '}' PDFDoc.LF ...
        '\toprule' PDFDoc.LF ...
        title '\\' PDFDoc.LF ...
        '\midrule' PDFDoc.LF];
    tail = [
        '\bottomrule' PDFDoc.LF...
        notes ...
        '\end{tabular}' PDFDoc.LF...
        '\end{table}' PDFDoc.LF];
end

rowcolors = {'cone', 'ctwo'};   % these strings can not contain any digit
end

